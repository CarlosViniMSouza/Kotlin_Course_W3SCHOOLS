What is Kotlin?

    Kotlin is a modern, trending programming language that was released in 2016 by JetBrains.

    It has become very popular since it is compatible with Java (one of the most popular programming
languages out there), which means that Java code (and libraries) can be used in Kotlin programs.

Kotlin is used for:

    1 -> Mobile applications (specially Android apps)
    2 -> Web development
    3 -> Server side applications
    4 -> Data science

    ... And much, much more!

Why Use Kotlin?

    1 -> Kotlin works on different platforms (Windows, Mac, Linux, Raspberry Pi, etc.)
    2 -> Kotlin is fully compatible with Java
    3 -> Kotlin is concise and safe
    4 -> Kotlin is easy to learn, especially if you already know Java
    5 -> Kotlin is free to use
    6 -> Big community/support
    7 -> And More: Actually, is the favorite language for domain the Android Native Development
    (with empowered Google)
